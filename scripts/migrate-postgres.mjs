async function createSchema() {
  await pool.query(`
    CREATE TABLE IF NOT EXISTS users (
      id INTEGER PRIMARY KEY,
      name TEXT NOT NULL,
      target_band TEXT NOT NULL DEFAULT '7.0',
      daily_target INTEGER NOT NULL DEFAULT 12,
      created_at TIMESTAMPTZ NOT NULL,
      updated_at TIMESTAMPTZ NOT NULL
    );

    CREATE TABLE IF NOT EXISTS words (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      word TEXT NOT NULL UNIQUE,
      phonetic TEXT NOT NULL,
      pos TEXT NOT NULL,
      meaning TEXT NOT NULL,
      english TEXT NOT NULL,
      example TEXT NOT NULL,
      example_cn TEXT NOT NULL,
      collocations TEXT[] NOT NULL,
      synonyms TEXT[] NOT NULL,
      topic TEXT NOT NULL,
      level TEXT NOT NULL CHECK (level IN ('基础', '高频', '进阶')),
      source TEXT NOT NULL DEFAULT 'IELTS academic high-frequency seed',
      created_at TIMESTAMPTZ NOT NULL
    );

    CREATE TABLE IF NOT EXISTS user_word_progress (
      user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
      word_id INTEGER NOT NULL REFERENCES words(id) ON DELETE CASCADE,
      status TEXT NOT NULL CHECK (status IN ('new', 'learning', 'mastered')),
      seen INTEGER NOT NULL DEFAULT 0,
      correct INTEGER NOT NULL DEFAULT 0,
      last_seen TIMESTAMPTZ NOT NULL,
      updated_at TIMESTAMPTZ NOT NULL,
      PRIMARY KEY (user_id, word_id)
    );

    CREATE TABLE IF NOT EXISTS study_sessions (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
      word_id INTEGER REFERENCES words(id) ON DELETE SET NULL,
      action TEXT NOT NULL,
      correct INTEGER,
      created_at TIMESTAMPTZ NOT NULL
    );
  `);
}

async function migrateUsers() {
  const now = new Date().toISOString();
  const users = existsSync(sqlitePath)
    ? new DatabaseSync(sqlitePath).prepare('SELECT * FROM users').all()
    : [{ id: 1, name: 'IELTS Learner', target_band: '7.0', daily_target: 12, created_at: now, updated_at: now }];

  for (const user of users) {
    await pool.query(
      `INSERT INTO users (id, name, target_band, daily_target, created_at, updated_at)
       VALUES ($1, $2, $3, $4, $5, $6)
       ON CONFLICT (id) DO UPDATE SET
        name = EXCLUDED.name,
        target_band = EXCLUDED.target_band,
        daily_target = EXCLUDED.daily_target,
        updated_at = EXCLUDED.updated_at`,
      [user.id, user.name, user.target_band, user.daily_target, user.created_at || now, user.updated_at || now],
    );
  }
}

async function migrateWords() {
  const now = new Date().toISOString();
  const sqliteWords = existsSync(sqlitePath)
    ? new DatabaseSync(sqlitePath).prepare('SELECT * FROM words ORDER BY id').all()
    : [];
  const jsonWords = sqliteWords.length ? [] : [...loadVocabulary(curatedVocabularyPath), ...loadVocabulary(expandedVocabularyPath)];

  for (const word of sqliteWords) {
    await upsertWord({
      id: word.id,
      word: word.word,
      phonetic: word.phonetic,
      pos: word.pos,
      meaning: word.meaning,
      english: word.english,
      example: word.example,
      exampleCn: word.example_cn,
      collocations: word.collocations.split('|'),
      synonyms: word.synonyms.split('|'),
      topic: word.topic,
      level: word.level,
      source: word.source,
      createdAt: word.created_at || now,
    });
  }

  for (const [index, word] of jsonWords.entries()) {
    await upsertWord({ id: index + 1, ...word, createdAt: now });
  }
}

async function upsertWord(word) {
  await pool.query(
    `INSERT INTO words
      (id, word, phonetic, pos, meaning, english, example, example_cn, collocations, synonyms, topic, level, source, created_at)
     VALUES ($1, $2, $3, $4, $5, $6, $7, $8, $9::text[], $10::text[], $11, $12, $13, $14)
     ON CONFLICT (word) DO UPDATE SET
      phonetic = EXCLUDED.phonetic,
      pos = EXCLUDED.pos,
      meaning = EXCLUDED.meaning,
      english = EXCLUDED.english,
      example = EXCLUDED.example,
      example_cn = EXCLUDED.example_cn,
      collocations = EXCLUDED.collocations,
      synonyms = EXCLUDED.synonyms,
      topic = EXCLUDED.topic,
      level = EXCLUDED.level,
      source = EXCLUDED.source`,
    [
      word.id,
      word.word,
      word.phonetic,
      word.pos,
      word.meaning,
      word.english,
      word.example,
      word.exampleCn,
      word.collocations,
      word.synonyms,
      word.topic,
      word.level,
      word.source,
      word.createdAt,
    ],
  );
}

async function migrateProgress() {
  if (!existsSync(sqlitePath)) return;
  const rows = new DatabaseSync(sqlitePath).prepare('SELECT * FROM user_word_progress').all();
  for (const row of rows) {
    await pool.query(
      `INSERT INTO user_word_progress (user_id, word_id, status, seen, correct, last_seen, updated_at)
       VALUES ($1, $2, $3, $4, $5, $6, $7)
       ON CONFLICT (user_id, word_id) DO UPDATE SET
        status = EXCLUDED.status,
        seen = EXCLUDED.seen,
        correct = EXCLUDED.correct,
        last_seen = EXCLUDED.last_seen,
        updated_at = EXCLUDED.updated_at`,
      [row.user_id, row.word_id, row.status, row.seen, row.correct, row.last_seen, row.updated_at],
    );
  }
}

async function migrateSessions() {
  if (!existsSync(sqlitePath)) return;
  const rows = new DatabaseSync(sqlitePath).prepare('SELECT * FROM study_sessions ORDER BY id').all();
  for (const row of rows) {
    await pool.query(
      `INSERT INTO study_sessions (id, user_id, word_id, action, correct, created_at)
       VALUES ($1, $2, $3, $4, $5, $6)
       ON CONFLICT (id) DO NOTHING`,
      [row.id, row.user_id, row.word_id, row.action, row.correct, row.created_at],
    );
  }
}

async function resetSequences() {
  await pool.query(`SELECT setval(pg_get_serial_sequence('words', 'id'), COALESCE((SELECT MAX(id) FROM words), 1), true)`);
  await pool.query(
    `SELECT setval(pg_get_serial_sequence('study_sessions', 'id'), COALESCE((SELECT MAX(id) FROM study_sessions), 1), true)`,
  );
}

function loadVocabulary(path) {
  if (!existsSync(path)) return [];
  const words = JSON.parse(readFileSync(path, 'utf8'));
  const source = path.endsWith('ielts_curated_words.json')
    ? 'IELTS academic curated seed'
    : 'Oxford/Common English + WordNet generated IELTS expansion';
  return Array.isArray(words) ? words.map((word) => ({ ...word, source })) : [];
}
